def suma(lista: list):
    return sum(lista)

def maximo(lista: list):
    return max(lista)

def minimo(lista: list):
    return min(lista)

lista = [5,7,2,55,99,222,1,34,4,65,43,0,2,1]

print("La suma total es: ",suma(lista))
print("El mayor numero de la lista es: ",maximo(lista))
print("El menor numero de la lista es: ",minimo(lista))
